/// what is a library and a package
/// library -  is a set of reusable code created by others which we can use by importing them.
///
/// Package -  is usually a library or multiple libraries which also contains pubspec.yaml file other resources such as assets
/// configuration files etc. To use a package in dart files we include it as a dependency in the pubspec.yaml file and run pub get to
/// fetch its dependencies
///
/// what is pubspec.yaml file?
/// Its a configuration settings file for a dart package. It has essential information about package structure, dependencies and configuration settings.
/// It is used by dart package manager pub to manage dependencies.
/// All packages can be published in pub.dev
///
/// what is YAML file?
